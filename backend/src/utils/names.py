"""Детерминированная генерация имён покупателей для отображения в отзывах.

Реальных имён в исходных данных нет — но показывать "Покупатель #71237" вместо
живого имени выглядит неубедительно для витрины. Имя генерируется стабильно по
review_id (один и тот же отзыв всегда получает одно и то же имя между запусками).
"""

import hashlib

MALE_NAMES = [
    "Алексей", "Дмитрий", "Сергей", "Андрей", "Максим", "Иван", "Артём", "Никита",
    "Егор", "Владимир", "Павел", "Роман", "Кирилл", "Денис", "Игорь", "Михаил",
    "Виктор", "Антон", "Тимур", "Олег",
]

FEMALE_NAMES = [
    "Анна", "Мария", "Елена", "Ольга", "Наталья", "Екатерина", "Светлана", "Юлия",
    "Ирина", "Татьяна", "Дарья", "Анастасия", "Виктория", "Кристина", "Полина",
    "Марина", "Алина", "Евгения", "Валентина", "Ксения",
]

SURNAME_INITIALS = list("АБВГДЕЖЗИКЛМНОПРСТУФХЦЧШЩЭЮЯ")


def generate_reviewer_name(seed: int) -> str:
    """Возвращает имя вида 'Алексей М.' стабильно по переданному seed (обычно review_id)."""
    h = int(hashlib.md5(str(seed).encode()).hexdigest(), 16)
    is_male = (h % 2) == 0
    names = MALE_NAMES if is_male else FEMALE_NAMES
    name = names[(h // 2) % len(names)]
    initial = SURNAME_INITIALS[(h // 97) % len(SURNAME_INITIALS)]
    return f"{name} {initial}."
